<template>
  <ag-grid-vue style="width: 500px; height: 500px;"
    class="ag-theme-alpine"
    :columnDefs="columnDefs"
    :rowData="rowData">
  </ag-grid-vue>
</template>

<script>
import { AgGridVue } from 'ag-grid-vue3'

export default {
  name: 'App',
  data () {
    return {
      columnDefs: null,
      rowData: null
    }
  },
  components: {
    AgGridVue
  },
  beforeMount () {
    this.columnDefs = []
    this.rowData = [
      { make: 'Toyota', model: 'Celica', price: 35000 },
      { make: 'Ford', model: 'Mondeo', price: 32000 },
      { make: 'Porsche', model: 'Boxter', price: 72000 }
    ]
    for (const val of this.rowData[0]) {
      this.columnDefs.push({field: val})
      }
  }
}
</script>

